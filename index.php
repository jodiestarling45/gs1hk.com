                                                <p>&nbsp;</p>
                                                <p><strong>Background</strong></p>
                                                <p>The Retail has been GS1’s “bread and butter” since GS1 was
                                                    created.&nbsp; Over the last few years however, several
                                                    disruptions in retail are reshaping the sector, supply chains
                                                    and retailer investment strategies.&nbsp; Some of these
                                                    disruptions include.</p>
