                                                    <p><strong>Facilitator</strong></p>

                                                    <p>Marcel Sieira, Head Customer Engagement, GS1 Australia</p>

                                                    <p>&nbsp;</p>

                                                    <p><strong>Background</strong></p>

                                                    <p>The Retail has been GS1’s “bread and butter” since GS1 was
                                                        created.&nbsp; Over the last few years however, several
                                                        disruptions in retail are reshaping the sector, supply chains
                                                        and retailer investment strategies.&nbsp; Some of these
                                                        disruptions include.</p>

                                                    <ul>
                                                        <li>Increase in Online retail and the empowered consumer</li>
                                                        <li>Growth of marketplaces and platforms (Amazon, Alibaba,
                                                            eBay)
                                                        </li>
                                                        <li>New market entrants (in Australia – Kaufland: <em>Germany
                                                                hypermarket chain</em> <em>offering a wide range of
                                                                products across all price categories – from premium to
                                                                budget</em>, IRexchange: <em>a disruptive technology
                                                                and</em> <em>next-generation business removing the role
                                                                and associated costs of conventional wholesalers or
                                                                “middlemen”</em>, etc)
                                                        </li>
                                                        <li>Fragmentation of established retailer (in Australia –
                                                            Metcash: <em>leading wholesale distribution and marketing
                                                                company</em>)
                                                        </li>
                                                        <li>Disruptive technologies (including Blockchain, IoT, Data
                                                            Analytics, etc)
                                                        </li>
                                                        <li>Increased regulation (in Australia – Country of Origin
                                                            Labelling, Front of Pack Labelling, container deposit
                                                            schemes, sugar declarations, etc)
                                                        </li>
                                                        <li>Emergence of “Hyper Convenience” and fulfillment/last-mile
                                                            opportunities; to name but only a few
                                                        </li>
                                                    </ul>

                                                    <p>&nbsp;</p>

                                                    <p>To ensure we remain relevant and adding value to our members in
                                                        the retail sector, GS1 needs to, on an ongoing basis, review our
                                                        value propositions and market approaches considering these
                                                        disruptions.</p>

                                                    <p>Over the past 8 months, GS1 Australia has been re-assessing and
                                                        updating its go-to-market approach for Retail which has led to
                                                        the establishment of 4 strategic pillars that will drive
                                                        engagement in the retail sector:</p>

                                                    <ol>
                                                        <li>Building and sustaining digital capability;</li>
                                                        <li>Excellence in B2C Fulfilment;</li>
                                                        <li>Getting supply chain basics right; and</li>
                                                        <li>Trust and Integrity</li>
                                                    </ol>

                                                    <p>&nbsp;</p>

                                                    <p><strong>Scope &amp; Objective</strong></p>

                                                    <p>This deep dive session will focus on how GS1 engages in Retail,
                                                        how we can create a structured approach for communicating our
                                                        value proposition and strategies for market engagement using the
                                                        newly develop Retail strategy as a basis for discussion.</p>

