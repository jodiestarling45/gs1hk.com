                                                <p><strong>Facilitator</strong></p>
                                                <p>Nick Allison, General Manager, Government, GS1 New Zealand<br>
                                                    &nbsp;</p>
                                                <p><strong>Background</strong></p>
                                                <p>The provision of accurate and timely data to consumers about food
                                                    products has and continues to be a challenge for governments and
                                                    industry.&nbsp; There is an ever-increasing demand for food
                                                    product data by consumers, increasing needs by governments to
                                                    address obesity and other health issues relating to diets, and
                                                    increasing needs by brands to provide information to consumers
                                                    to differentiate in the market.</p>
                                                <p>&nbsp;</p>
                                                <p>Today two billion smart phones can natively scan codes helping
                                                    consumers to interact digitally with products. By digitally
                                                    linking to web-based information about content it is possible to
                                                    enable access to information for consumers and support regional
                                                    harmonization efforts. This approach will ultimately create a
                                                    new channel for product information sharing enabling all types
                                                    of content to be provided to consumers, based on open, Global
                                                    Standards.</p>
                                                <p>&nbsp;</p>
                                                <p><strong>Scope &amp; Objective</strong></p>
                                                <p>There are more and more public-private consortiums which provide
                                                    digital twin product data to both regulators and businesses.
                                                    There are also institutions providing data to retailers. This
                                                    deep dive session will explore how GS1 can cooperate with
                                                    industry and Government to make available online product data in
                                                    a harmonized way for the benefit of Asia-Pacific consumers and
                                                    Government.</p>
                                                <p>Focus will be on what current and future requirements for label
                                                    information exist in Asia-Pacific, how we are working with
                                                    partners and industry to address these issues, how GS1 standards
                                                    such as Digital Link will support the demands and what
                                                    initiatives are currently underway.</p>
