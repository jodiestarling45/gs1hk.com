                                                    <p><strong>Background</strong></p>

                                                    <p>Cross-border e-commerce is one of the fastest growing segments of
                                                        global trade. The Asia-Pacific region has the biggest volume of
                                                        sales with cross-border B2C e-commerce reaching 144 billion USD
                                                        in 2016, accounting for 35.9% of worldwide cross-border B2C
                                                        e-commerce sales. The figures are estimated to be 476 billion
                                                        USD and 47.9% in 2020. This growth is causing the rise in volume
                                                        of small shipments and adding complexity to border agencies. As
                                                        e-commerce grows, more parcels, new parties and more automated
                                                        systems will be involved, which will trigger a strong need for
                                                        regulation, standards and supporting processes.</p>

                                                    <p>&nbsp;</p>

                                                    <p>Currently there is a lack of coherent regulations and approaches
                                                        from governments on cross-border e-commerce. Regional efforts
                                                        should be made among GS1 Asia-Pacific to assist APEC Governments
                                                        to understand the potential benefits of integrating GS1
                                                        standards when developing automated cross-border e-commerce
                                                        systems, with the aim of promoting better alignment among
                                                        Governments throughout the Asia-Pacific region and ensure wide
                                                        adoption of GS1 standards.</p>

                                                    <p>&nbsp;</p>

                                                    <p><strong>Scope &amp; Objective</strong></p>

                                                    <p>This deep dive session will explain the compelling case for GS1
                                                        standards to underpin fast clearance for business, duty
                                                        collection and risk management for agencies, and safety for
                                                        consumers.</p>

                                                    <p>We will cover the recent developments in the area of cross-border
                                                        e-commerce focusing on what regulatory developments are
                                                        triggering changes in EU and Asia-Pacific with focus on how GS1
                                                        is positioned and potentially leveraged in these
                                                        developments.</p>

                                                    <p>We will work to understand relevant data privacy issues and
                                                        frameworks, including developing frameworks such as the WTO
                                                        e-commerce negotiations, and how GS1 standards and solutions can
                                                        help underpin cross-border e-commerce in Asia-Pacific.</p>

