                                                <p>&nbsp;</p>
                                                <p><strong>Background</strong></p>
                                                <p>Governments are starting to require the use of e-invoicing
                                                    systems for public procurement and related activities.
                                                    Countrywide projects are launched by authorities with the aim of
                                                    reducing tax evasion. Suppliers and buyers have to send either
                                                    invoice data or at least reports in electronic format to the tax
                                                    authorities for real-time validation &amp; auditing. Supporting
                                                    this, the Pan-European Public Procurement On-Line (PEPPOL) is a
                                                    set of open standards and specifications which enables the
                                                    exchange of standardised machine-readable documents between
                                                    business partners. Some of the documents it can carry include:
                                                    eInvoice, e-Advance Shipping Notes, e-Catalogues, Message Level
                                                    Responses and eOrders. PEPPOL does in many cases include GLN for
                                                    identification of economic operators. However, other
                                                    identification systems such as local tax ID etc. can also be
                                                    used, exclusively or as hybrid. The PEPPOL standard is
                                                    maintained by OpenPEPPOL, a non-profit international association
                                                    that GS1 Global has an MOU with.</p>
                                                <p>&nbsp;</p>
                                                <p>In a number of early mover countries in Asia-Pacific, Singapore,
                                                    Australia and New Zealand PEPPOL has been the system of choice
                                                    with implementation during 2019. Further along, Brazil has
                                                    achieved the highest market penetration (&gt;90%) globally for
                                                    electronic invoices in the B2B/B2G segments. Almost 1 billion
                                                    electronic invoices are exchanged every month in the
                                                    country.</p>
